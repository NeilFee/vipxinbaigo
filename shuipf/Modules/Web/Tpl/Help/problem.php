							<span class="ask-wd">11.问：如何升级成为N-VIP金钻卡会员？</span>
							<span class="ans-wd">答：金钻会员升级仅限受邀会员。受邀会员可凭邀请短信，持有效身份证件及原N-VIP卡至原发卡店顾客服务中心申请办理。</span>
						</li>
